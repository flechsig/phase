      subroutine hyperbola_8(a0,b0,w00,u00,phi,a)
      real*8 a0, b0, w00, u00, phi, a
      real*8 cosa,sina,l22,w11,c33,w12
      dimension a(0:8,0:8)
      cosa= cos(phi)
      sina= sin(phi)
      l22=-cosa**2*(a0**2+b0**2)+b0**2
      w11=2*b0**2*(u00*sina-w00*cosa)
      c33=-cosa*w00*w11+sina*u00*w11+2*b0**2*l22
      w12=-sqrt(2.0)*cosa*sina*a0**2-sqrt(2.0)*cosa*sina*b0**2
      c44=-sqrt(2.0)*cosa*a0**2*u00+sqrt(2.0)*sina*b0**2*w00
      a(0.0,0.0)=(sqrt(c33)*a0+c44)/(sqrt(2.0)*l22)
      a(0.0,1.0)=0.0
      a(0.0,2.0)=a0/(sqrt(c33)*sqrt(2.0))
      a(0.0,3.0)=0.0
      a(0.0,4.0)=(-a0*l22)/(2.0*sqrt(c33)*sqrt(2.0)*c33)
      a(0.0,5.0)=0.0
      a(0.0,6.0)=(a0*l22**2)/(2.0*sqrt(c33)*sqrt(2.0)*c33**2)
      a(0.0,7.0)=0.0
      a(1.0,0.0)=(sqrt(c33)*w12+a0*w11)/(sqrt(c33)*sqrt(2.0)*l22)
      a(1.0,1.0)=0.0
      a(1.0,2.0)=(-a0*w11)/(sqrt(c33)*sqrt(2.0)*c33)
      a(1.0,3.0)=0.0
      a(1.0,4.0)=(3.0*a0*l22*w11)/(2.0*sqrt(c33)*sqrt(2.0)*c33**2)
      a(1.0,5.0)=0.0
      a(1.0,6.0)=(-5.0*a0*l22**2*w11)/(2.0*sqrt(c33)*sqrt(2.0)*c33**3
     . )
      a(2.0,0.0)=(2.0*a0*b0**2*c33-a0*w11**2)/(2.0*sqrt(c33)*sqrt(2.0
     . )*c33*l22)
      a(2.0,1.0)=0.0
      a(2.0,2.0)=(-2.0*a0*b0**2*c33+3.0*a0*w11**2)/(2.0*sqrt(c33)*
     . sqrt(2.0)*c33**2)
      a(2.0,3.0)=0.0
      a(2.0,4.0)=(6.0*a0*b0**2*c33*l22-15.0*a0*l22*w11**2)/(4.0*sqrt(
     . c33)*sqrt(2.0)*c33**3)
      a(2.0,5.0)=0.0
      a(3.0,0.0)=(-2.0*a0*b0**2*c33*w11+a0*w11**3)/(2.0*sqrt(c33)*
     . sqrt(2.0)*c33**2*l22)
      a(3.0,1.0)=0.0
      a(3.0,2.0)=(6.0*a0*b0**2*c33*w11-5.0*a0*w11**3)/(2.0*sqrt(c33)*
     . sqrt(2.0)*c33**3)
      a(3.0,3.0)=0.0
      a(3.0,4.0)=(-30.0*a0*b0**2*c33*l22*w11+35.0*a0*l22*w11**3)/(4.0
     . *sqrt(c33)*sqrt(2.0)*c33**4)
      a(4.0,0.0)=(-4.0*a0*b0**4*c33**2+12.0*a0*b0**2*c33*w11**2-5.0*
     . a0*w11**4)/(8.0*sqrt(c33)*sqrt(2.0)*c33**3*l22)
      a(4.0,1.0)=0.0
      a(4.0,2.0)=(12.0*a0*b0**4*c33**2-60.0*a0*b0**2*c33*w11**2+35.0*
     . a0*w11**4)/(8.0*sqrt(c33)*sqrt(2.0)*c33**4)
      a(4.0,3.0)=0.0
      a(5.0,0.0)=(12.0*a0*b0**4*c33**2*w11-20.0*a0*b0**2*c33*w11**3+
     . 7.0*a0*w11**5)/(8.0*sqrt(c33)*sqrt(2.0)*c33**4*l22)
      a(5.0,1.0)=0.0
      a(5.0,2.0)=(-60.0*a0*b0**4*c33**2*w11+140.0*a0*b0**2*c33*w11**3
     . -63.0*a0*w11**5)/(8.0*sqrt(c33)*sqrt(2.0)*c33**5)
      a(6.0,0.0)=(8.0*a0*b0**6*c33**3-60.0*a0*b0**4*c33**2*w11**2+
     . 70.0*a0*b0**2*c33*w11**4-21.0*a0*w11**6)/(16.0*sqrt(c33)*sqrt(
     . 2.0)*c33**5*l22)
      a(6.0,1.0)=0.0
      a(7.0,0.0)=(-40.0*a0*b0**6*c33**3*w11+140.0*a0*b0**4*c33**2*w11
